           <h4>O que é a Psicoterapia?</h4>
         	<p>
            A Psicoterapia é um processo conduzido por um psicólogo que tem por objetivo a compreensão do ser humano e de seu modo de existir no mundo ampliando a consciência que o paciente tem de si mesmo, aprendendo e se desenvolvendo como pessoa, permitindo-lhe tomar o rumo da própria vida disponibilizando a oportunidade de superar qualquer obstáculo que esteja dificultando o seu viver.
			</p>
            <h4>Qual o objetivo da Psicoterapia?</h4>
         	<p>
            O objetivo da Psicoterapia é dar condições para que o cliente desenvolva em conjunto com o Psicólogo o autoconhecimento abordando vários aspectos de sua vida, desde reflexões sobre sua existência, até tratamentos de doenças emocionais e psicopatológicas. O Psicoterapeuta promove o desenvolvimento de seus clientes gerando novos olhares sobre suas vidas, ocasionando na superação de seus desafios, trazendo grande alívio, conforto e segurança.
			</p>
            <h4>Qual a diferença da psiquiatria?</h4>
         	<p>
            A psiquiatria é uma das vertentes da medicina que tem por objetivo diagnosticar e tratar organicamente pessoas gravemente comprometidas em seu estado mental. O tratamento da doença e o diagnóstico são baseados na terapia medicamentosa do modelo médico.
			</p>
            <h4>Qual o papel do psicólogo?</h4>
         	<p>
            A atuação do psicólogo deve ser pautada na escuta do cliente em toda sua complexidade, com sinceridade, respeito e ética. Devemos servi-lo como um facilitador do seu processo de desenvolvimento compreendendo seus medos, angústias e\ou fantasias de uma maneira delicada e sutil. É ajudá-lo a se ajudar, sem violar seus direitos pessoais.
			</p>
			<p>
Assim, a pessoa terá a oportunidade de entrar em um contato mais profundo consigo mesma, compreendendo de modo mais realista suas capacidades, sentimentos, necessidades e limitações tornando-se mais segura e confiante para enfrentar seus problemas, podendo assim resolver suas dificuldades de modo mais satisfatório e maduro.
			</p>
             <h4>Quando se deve procurar um psicoterapeuta?</h4>
         	<p>
            Quando a pessoa não está se sentido feliz, ou insatisfeita com algum aspecto de sua vida, passando por alguma situação crítica. O ideal seria a busca do tratamento psicoterápico para prevenção de qualquer conflito que a pessoa esteja vivendo, já que sempre passaremos por processos conflituosos em nossas vidas, é importante se conhecer para prevenir futuros sofrimentos. 
